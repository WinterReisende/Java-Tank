    // 构建函数中给出一个文件路径，读取文件内容创建地图
    public synchronized void setMapFromSources(String filePath) {
        // this.model = model;
        // this.mapList = null;
        this.map = new String[400];
        // 打开文件
        try {
            InputStream inputStream = (InputStream) getClass().getClassLoader()
                    .getResourceAsStream("config/" + filePath + ".txt");
            BufferedReader reader = new BufferedReader(new InputStreamReader(inputStream));
            String line;
            int j = 0;
            while ((line = reader.readLine()) != null) {
                this.map[j++] = line.substring(0, 2);
            }
            for (int i = 0; i < 400; i++) {
                assert (this.map[i].equals("##") || this.map[i].equals("__"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    // 把地图写入文件
    public synchronized void writeMapToFile(String filePath) {
        try {
            java.io.File file = new java.io.File(filePath + ".txt");
            java.io.PrintWriter output = new java.io.PrintWriter(file);
            for (int i = 0; i < 400; i++) {
                output.println(this.map[i]);
            }
            output.close();
        } catch (FileNotFoundException e) {
            System.out.println("文件未找到: " + e.getMessage());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    // 从文件读入地图
    public synchronized void setMapFromFile(String filePath) {
        try {
            File file = new File(filePath + ".txt");
            BufferedReader reader = new BufferedReader(
                    new InputStreamReader(new FileInputStream(file), StandardCharsets.UTF_8));
            String line;
            int i = 0;
            while ((line = reader.readLine()) != null) {
                // 处理每一行数据
                this.map[i++] = line.substring(0, 2);
            }
        } catch (FileNotFoundException e) {
            System.out.println("文件未找到: " + e.getMessage());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

